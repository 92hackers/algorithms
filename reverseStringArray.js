const reverseString = function(s) {
  const size = s.length
  const lastIndex = size - 1;

  const halfSize = size / 2;
  const absHalfSize = parseInt(halfSize)

  const middleIndex = halfSize === absHalfSize ? absHalfSize - 1 : absHalfSize

  
  for (let i=0; i <= middleIndex; i++) {
    var j = s[i]

    s[i] = s[lastIndex - i]
    s[lastIndex - i] = j

  }
};

